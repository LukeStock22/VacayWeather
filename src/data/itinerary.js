export const TRIP_LOCATIONS = Array.from(
  new Map(TRIP_STOPS.map((stop) => [stop.locationId, stop])).values(),
)

export const TRIP_DAYS = Array.from(
  TRIP_STOPS.reduce((days, stop) => {
    const existingDay = days.get(stop.date)
    if (existingDay) {
      existingDay.stops.push(stop)
      return days
    }

    days.set(stop.date, {
      date: stop.date,
      stops: [stop],
    })
    return days
  }, new Map()).values(),
)
